/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
 //HAL_GPIO_WritePin(Test_Prob_GPIO_Port,Test_Prob_Pin,GPIO_PIN_SET);  //testing flag

	LED_timer++;
	
	System_time.ms++;
	
  if (System_time.ms==1000)
	{
	  System_time.ms = 0;
		System_time.Sec++;
	  
	  if (System_time.Sec == Seconds_Per_Day) // the final value should be 86400s for a day *nan teamperary
	  {
	    System_time.Sec = 0;
			System_time.Day++;
			if (System_time.Day == Days_Per_Period )
			 {
			   System_time.Day = 0;
				 Short_test_En = 1;  
				 System_time.Period ++;
				 if ( System_time.Period == Long_Test_Period_Num )
				  {
				    Short_test_En = 0;  
						Long_test_En = 1;
				  }
			 }
		}
	
	}

	
//HAL_GPIO_WritePin(Test_Prob_GPIO_Port,Test_Prob_Pin,GPIO_PIN_RESET);  //testing flag			

  /* USER CODE END SysTick_IRQn 1 */
}

